// অ্যারেতে সর্বোচ্চ সংখ্যাটি খুঁজে বের করার জন্য ফাংশন
function findHighestNumber(arr) {
    // প্রথম সংখ্যাটিকে সর্বোচ্চ ধরে নেওয়া হচ্ছে
    let highest = arr[0];
    
    // লুপ দিয়ে পুরো অ্যারে যাচাই করা হচ্ছে
    for (let i = 1; i < arr.length; i++) {
        // যদি বর্তমান সংখ্যা সর্বোচ্চ সংখ্যার চেয়ে বড় হয়, তাহলে সেটিকে সর্বোচ্চ হিসেবে আপডেট করা হচ্ছে
        if (arr[i] > highest) {
            highest = arr[i];
        }
    }
    
    // সর্বোচ্চ সংখ্যাটি ফেরত দিচ্ছে
    return highest;
}

// নম্বরের একটি উদাহরণ অ্যারে
let numbers = [10, 5, 8, 20, 3, 15];

// ফাংশনটি কল করে এবং ফলাফল প্রিন্ট করা হচ্ছে
console.log("সর্বোচ্চ সংখ্যা হলো: " + findHighestNumber(numbers));
